        $games = $this->entityManager->getRepository(Digiseller::class)->getByCriterias(['stock' => 'stock']);
        return $this->render('stock.html.twig', [
            'flag' => 'stock',
            'games' => $games,
            'title' => '  '
        ]);
    }

    /**
     * @Route("/get-comments/{digiseller}", name="get-comments")
     * @ParamConverter("digiseller", options={"mapping": {"digiseller": "gameId"}})
     */
    public function getComments(Request $request, Digiseller $digiseller)
    {
        $page = $request->query->get('page', 1);
        $comments = $this->digisellerApi->GetComments($digiseller->getSeller(), $digiseller->getGameId(), $page);
        foreach ($comments['review'] as $resp) {
            $reviews[] = [
                'type' => $resp['type'],
                'date' => $resp['date'],
                'info' => $resp['info'],
                'comment' => $resp['comment'],
            ];
        }
        return $this->render('common/_game_reviews.html.twig', [
            'digiseller' => $digiseller,
            'reviews' => $reviews,
        ]);
    }

    /**
     * @Route("/product/{digiseller}-zaka-zaka", name="game")
     */
    public function game(string $digiseller)
    {
        $digi = $this->entityManager->getRepository(Digiseller::class)->findOneBy(['gameId' => $digiseller]);

        if (!$digi) {
            if (key_exists($digiseller, $this->missingGame)) {
                return $this->redirectToRoute('catalog', ['url' => $this->missingGame[$digiseller]]);
            } else {
                throw $this->createNotFoundException();
            }
        }
        $digiseller = $digi;

        $category = $this->entityManager->getRepository(GgselCategory::class)->findOneBy(['digiCatalog' => $digiseller->getCategory()]);
        $related = $this->entityManager->getRepository(Digiseller::class)->getRelated($digiseller);
        $seller = $this->entityManager->getRepository(Seller::class)->findOneBy(['sellerId' => $digiseller->getSeller()]);
        return $this->render('game.html.twig', [
            'category' => $category,
            'game' => $digiseller,
            'related' => $related,
            'seller' => $seller,
        ]);
    }


